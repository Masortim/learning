#include <iostream>
#include <Windows.h>

int main()
{
	unsigned num;
	for (num = 0; num < 16; num++)	// цикл будет работать от 0 до 16
  	{
		printf("%o", num);	// восьмеричная система, левый столб
		printf("%x", num);	// шестнадцатеричная система со строчными буквами, средний столб
		printf("%X\n", num);	// шестнадцатеричная система с заглавными буквами, правый столб с переносом на следующую строку
	}
	system ("pause");
	return 0;
}
